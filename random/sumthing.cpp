int main()
{
    // Declare the variables
    char i;
 
    // Display the alphabets
    cout << "The Alphabets from A to Z are: \n";
 
    // Traverse each character
    // with the help of for loop
    for (i = 'A'; i <= 'Z'; i++) {
        // Print the alphabet
        cout << i << " ";
    }
    // Display the alphabets
    cout << "\nThe Alphabets from a to z are: \n";
    for (i = 'a'; i <= 'z'; i++) {
        // Print the alphabet
        cout << i << " ";
    }
 
    return 0;
}